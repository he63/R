count <- 1
myf <- function(x, wt=T){
  cat(x,"(",count,")", "\n")
  Sys.sleep(1)
  if(wt) 
    r <- paste("*", x, "*")
  else
    r <- paste("#", x, "#")
  count <<- count + 1;
  return(r)
}
